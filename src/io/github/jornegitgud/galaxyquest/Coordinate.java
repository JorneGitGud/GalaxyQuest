package io.github.jornegitgud.galaxyquest;

/**
 * the Coordinate class is used on the {@link Tile} class.
 * it has x and y coordinates
 *
 */
public class Coordinate {
    public final int x;
    public final int y;


    public Coordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }

}
